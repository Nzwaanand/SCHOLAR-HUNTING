    private void jButtonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahActionPerformed
        try {
        String namaBeasiswa = jTextFieldNamaBeasiswa.getText().trim();
        String kategoriBeasiswa = jComboBoxKategoriBeasiswa.getSelectedItem().toString();
        String jenisBeasiswa = jTextFieldJenisBeasiswa.getText().trim();
        String deadline = jTextFieldDeadline.getText().trim();
        String status = jRadioButtonStatusBuka.isSelected() ? "Buka" : 
                        jRadioButtonStatusTutup.isSelected() ? "Tutup" : "";

        if (namaBeasiswa.isEmpty() || jenisBeasiswa.isEmpty() || deadline.isEmpty() || status.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String checkQuery = "SELECT COUNT(*) FROM beasiswa WHERE nama_beasiswa = ?";
        try (Connection conn = Database.connect();
             PreparedStatement checkStatement = conn.prepareStatement(checkQuery)) {

            checkStatement.setString(1, namaBeasiswa);
            ResultSet resultSet = checkStatement.executeQuery();
            resultSet.next();
            int count = resultSet.getInt(1);

            if (count > 0) {
                JOptionPane.showMessageDialog(this, "Nama beasiswa sudah ada di database!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            String insertQuery = "INSERT INTO beasiswa (nama_beasiswa, kategori, jenis, deadline, status) VALUES (?, ?, ?, ?, ?)";
            try (PreparedStatement insertStatement = conn.prepareStatement(insertQuery, Statement.RETURN_GENERATED_KEYS)) {
                insertStatement.setString(1, namaBeasiswa);
                insertStatement.setString(2, kategoriBeasiswa);
                insertStatement.setString(3, jenisBeasiswa);
                insertStatement.setString(4, deadline);
                insertStatement.setString(5, status);

                int rowsInserted = insertStatement.executeUpdate();
                if (rowsInserted > 0) {
                    try (ResultSet generatedKeys = insertStatement.getGeneratedKeys()) {
                        if (generatedKeys.next()) {
                            int generatedID = generatedKeys.getInt(1);
                            System.out.println("ID Beasiswa yang baru dibuat: " + generatedID);
                            jTextFieldIDBeasiswa.setText(String.valueOf(generatedID));
                        }
                    }
                    JOptionPane.showMessageDialog(this, "Beasiswa berhasil ditambahkan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menyimpan beasiswa.", "Error", JOptionPane.ERROR_MESSAGE);
                }

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

        loadTableData();
        clearTextFields();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_jButtonTambahActionPerformed

