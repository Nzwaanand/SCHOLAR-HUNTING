    private void loadDataUser() {
    try (Connection conn = Database.connect()) {
        String query = "SELECT * FROM profil";
        try (var statement = conn.prepareStatement(query);
             ResultSet resultSet = statement.executeQuery()) {

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0); // Kosongkan tabel

            while (resultSet.next()) {
                Object[] row = {
                    resultSet.getString("Nama"),
                    resultSet.getString("NIM_NISN"),
                    resultSet.getString("Alamat"),
                    resultSet.getString("Status"),
                    resultSet.getString("No_Hp"),
                    resultSet.getString("Email"),
                    resultSet.getString("TTL")
                };
                model.addRow(row);
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat data user: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
} 
     

