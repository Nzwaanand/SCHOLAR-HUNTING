    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
         int row = jTable1.getSelectedRow();  // Mendapatkan baris yang dipilih
        if (row != -1) {
            // Mengambil data dari baris yang dipilih
            int id = (int) jTable1.getValueAt(row, 0);  // ID Beasiswa
            String nama = (String) jTable1.getValueAt(row, 1);  // Nama Beasiswa
            String kategori = (String) jTable1.getValueAt(row, 2);  // Kategori
            String jenis = (String) jTable1.getValueAt(row, 3);  // Jenis Beasiswa
            Date deadline = (Date) jTable1.getValueAt(row, 4);  // Deadline
            String status = (String) jTable1.getValueAt(row, 5);  // Status

            // Memasukkan data ke dalam form input
            jTextFieldIDBeasiswa.setText(String.valueOf(id));
            jTextFieldNamaBeasiswa.setText(nama);
            jComboBoxKategoriBeasiswa.setSelectedItem(kategori);
            jTextFieldJenisBeasiswa.setText(jenis);
            jTextFieldDeadline.setText(new SimpleDateFormat("yyyy-MM-dd").format(deadline));

            // Menentukan status berdasarkan data yang dipilih
            if (status.equals("Buka")) {
                jRadioButtonStatusBuka.setSelected(true);
            } else {
                jRadioButtonStatusTutup.setSelected(true);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

